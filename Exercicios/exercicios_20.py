# Faça um Programa que verifique se uma letra digitada é vogal ou consoante.

usuario = str(input("Digite uma letra: ")).lower()

if usuario == 'a':
    print("A letra é vogal")
elif usuario == 'e':
    print("A letra é vogal")
elif usuario == 'i':
    print("A letra é vogal")
elif usuario == 'o':
    print("A letra é vogal")
elif usuario == 'u':
    print("A letra é vogal")
else:
    print("É consoante")
